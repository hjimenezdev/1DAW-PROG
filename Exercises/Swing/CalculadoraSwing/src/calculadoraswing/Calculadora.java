    private void jbBotonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBotonIgualActionPerformed
        double result = 0;
        double secondNumber = Double.parseDouble(jlPantalla.getText().replace(',', '.'));
        switch (typeCalculation) {
            case ADDITION -> result = firstNumber + secondNumber;
            case DIVISION -> result = firstNumber / secondNumber;
            case MULTIPLICATION -> result = firstNumber * secondNumber;
            case SUBTRACTION -> result = firstNumber - secondNumber;
        }
        toCalculate = true;
        añadirNumeroALaPantalla(result);
    }//GEN-LAST:event_jbBotonIgualActionPerformed

