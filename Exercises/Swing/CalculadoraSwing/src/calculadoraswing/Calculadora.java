    private void añadirNumeroALaPantalla (double numero) {
        String pantallaContent = jlPantalla.getText().replace(',', '.');
        double numeroPantalla = Double.parseDouble(pantallaContent);
        if (!pantallaContent.startsWith("0.") && pantallaContent.startsWith("0") || toCalculate) {
            double parteDecimal = numero % 1;
            if (parteDecimal == 0.0) {
                jlPantalla.setText("" + (int)numero); 
            } else{
                jlPantalla.setText("" + Math.round(numero * 100.0) / 100.0);
            }
            toCalculate = false;
        } else if (pantallaContent.length() < 9) {
            double parteDecimal = numeroPantalla % 1;
            if (parteDecimal == 0.0) {
                jlPantalla.setText(jlPantalla.getText() + (int)numero);
            } else {
                numeroPantalla += numero;
                jlPantalla.setText(""+Math.round(numeroPantalla * 100.0) / 100.0);
            }
        }
    }
    
    private void añadirComaALaPantalla () {
        String contenidoPantalla = jlPantalla.getText();
        if (!contenidoPantalla.endsWith(",")) {
            contenidoPantalla += ",";
            jlPantalla.setText(contenidoPantalla);
        }
    }
    
    private void setTypeCalculation(TypeCalculation newTypeCalculation) {
        typeCalculation = newTypeCalculation;
        String contendioPantalla = jlPantalla.getText().replace(',', '.');
        firstNumber = Double.parseDouble(contendioPantalla);
        toCalculate = true;
    }
    
    
    
    
    
    
    
    
    
    
    
    
